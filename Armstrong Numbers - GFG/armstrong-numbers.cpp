class Solution {
  public:
    string armstrongNumber(int n){
        // code here
        int a = n;
        int v = 0;
        while(a){
            int i = a%10;
            v += i*i*i;
            a /= 10;
        }
        return (n == v) ? "Yes" : "No";
        
    }
};

